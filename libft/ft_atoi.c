#include "libft.h"

int	ft_atoi(const char *str)
{
	int	r;
	int	s;
	int	i;

	r = 0;
	s = 0;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + (str[i] - 48);
		i++;
	}
	if (s % 2 == 1)
		return (r * -1);
	return (r);
}
